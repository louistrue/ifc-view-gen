/**
 * Reads Inter latin WOFF2 files from @fontsource/inter and writes
 * lib/inter-svg-font-embed-data.ts for self-contained SVG @font-face embedding.
 * Run: node scripts/generate-inter-svg-font-embed.mjs
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.resolve(__dirname, '..')
const filesDir = path.join(root, 'node_modules', '@fontsource', 'inter', 'files')
const outFile = path.join(root, 'lib', 'inter-svg-font-embed-data.ts')

const weights = [
    { weight: 400, file: 'inter-latin-400-normal.woff2' },
    { weight: 600, file: 'inter-latin-600-normal.woff2' },
    { weight: 700, file: 'inter-latin-700-normal.woff2' },
]

for (const { file } of weights) {
    const p = path.join(filesDir, file)
    if (!fs.existsSync(p)) {
        console.error(`Missing font file: ${p}\nInstall devDependency @fontsource/inter`)
        process.exit(1)
    }
}

const lines = [
    '/**',
    ' * Auto-generated by scripts/generate-inter-svg-font-embed.mjs',
    ' * Inter (latin subset) WOFF2 as base64 for SVG <style> embedding.',
    ' * Font: SIL Open Font License 1.1 — https://github.com/rsms/inter',
    ' */',
    '',
]

for (const { weight, file } of weights) {
    const buf = fs.readFileSync(path.join(filesDir, file))
    const b64 = buf.toString('base64')
    lines.push(`export const INTER_WOFF2_LATIN_${weight}_BASE64 = '${b64}'`)
    lines.push('')
}

fs.writeFileSync(outFile, lines.join('\n'), 'utf8')
console.log(`Wrote ${outFile} (${weights.length} weights)`)
